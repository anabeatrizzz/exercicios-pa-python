a = int(input("Digite um numero: "))
b = int(input("Digite outro numero: "))

print(f"{a} é igual a {b}?   Resposta: {a == b}")
print(f"{a} é diferente de {b}?   Resposta: {a != b}")
print(f"{a} é maior que {b}?   Resposta: {a > b}")
print(f"{a} é menor que {b}?   Resposta: {a < b}")
print(f"{a} é maior ou igual a {b}?   Resposta: {a >= b}")
print(f"{a} é menor ou igual {b}?   Resposta: {a <= b}")